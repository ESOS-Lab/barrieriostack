static inline void make_gpio_floating(unsigned int gpio, bool floating)
{
	if (floating)
		gpio_direction_input(gpio);
	else
		gpio_direction_output(gpio, 0);
}

static int modem_on(struct mipi_lli_boot_spi *boot)
{
	/* Make PS_HOLD_OFF floating (Hi-Z) for CP ON */
	make_gpio_floating(boot->gpio_cp_off, true);

	gpio_set_value(boot->gpio_cp_on, 0);
	msleep(100);

	gpio_set_value(boot->gpio_cp_reset, 0);
	msleep(500);

	gpio_set_value(boot->gpio_cp_on, 1);
	msleep(100);

	gpio_set_value(boot->gpio_cp_reset, 1);
	msleep(300);

	if(boot->gpio_cp_clk_en)
		gpio_set_value(boot->gpio_cp_clk_en, 1);

	pr_info("%s:%x %x %x %x\n", __func__, gpio_get_value(boot->gpio_cp_off), gpio_get_value(boot->gpio_cp_on),
			gpio_get_value(boot->gpio_cp_reset), gpio_get_value(boot->gpio_cp_status));
	return 0;
}

static int modem_off(struct mipi_lli_boot_spi *boot)
{
	if(boot->gpio_cp_clk_en)
		gpio_set_value(boot->gpio_cp_clk_en, 0);

	gpio_set_value(boot->gpio_cp_reset, 0);

	/* Make PS_HOLD LOW for CP OFF */
	make_gpio_floating(boot->gpio_cp_off, false);
	gpio_set_value(boot->gpio_cp_on, 0);

	pr_info("%s:%x %x %x %x\n", __func__, gpio_get_value(boot->gpio_cp_off), gpio_get_value(boot->gpio_cp_on),
			gpio_get_value(boot->gpio_cp_reset), gpio_get_value(boot->gpio_cp_status));
	return 0;
}

static int check_cp_status(unsigned int gpio_cp_status, unsigned int count)
{
	int ret = 0;
	int cnt = 0;

	while (1) {
		if (gpio_get_value(gpio_cp_status) != 0) {
			ret = 0;
			break;
		}

		cnt++;
		if (cnt >= count) {
			pr_err("ERR! gpio_cp_status == 0 (cnt %d)\n", cnt);
			ret = -EFAULT;
			break;
		}

		msleep(20);
	}

	return ret;
}

static inline int spi_send(struct mipi_lli_boot_spi *boot, const char *buff, unsigned len)
{
	int ret;
	struct spi_message msg;
	struct spi_transfer xfer = {
		.len = len,
		.tx_buf = buff,
	};

	spi_message_init(&msg);
	spi_message_add_tail(&xfer, &msg);

	ret = spi_sync(boot->spi_dev, &msg);
	if (ret < 0)
		pr_err("ERR! spi_sync fail (err %d)\n", ret);

	return ret;
}

static int spi_boot_write(struct mipi_lli_boot_spi *boot, const char *addr,
			  const long len)
{
	int ret = 0;

	pr_err("+++\n");

	ret = spi_send(boot, addr, len);
	if (ret < 0) {
		pr_err("ERR! spi_send fail (err %d)\n", ret);
		goto exit;
	}
exit:
	pr_err("---\n");
	return ret;
}

